    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "obrisete ovo iznajmljivanje?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                ClientController.getInstance().deleteIznajmljivanje(i);
                FormPretragaIznajmljivanje fp = (FormPretragaIznajmljivanje) getParent();
                fp.refreshTable();
                JOptionPane.showMessageDialog(this, "Uspesno obrisano iznajmljivanje.");
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(FormDetaljiIznajmljivanje.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        try {

            if (txtOpis.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Morate uneti opis!");
                return;
            }

            String opis = txtOpis.getText();

            TableModelStavkeIznajmljivanja tm = (TableModelStavkeIznajmljivanja) tblStavke.getModel();

            i.setOpis(opis);
            i.setUkupanIznos(ukupanIznos);
            i.setStavkeIznajmljivanja(tm.getLista());

            ClientController.getInstance().updateIznajmljivanje(i);
            FormPretragaIznajmljivanje fp = (FormPretragaIznajmljivanje) getParent();
            fp.refreshTable();
            JOptionPane.showMessageDialog(this, "Uspesno izmenjeno iznajmljivanje.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
//            Logger.getLogger(FormDetaljiIznajmljivanje.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnIzmeniActionPerformed

