    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed

        if (txtBrojDana.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate izracunati broj dana!");
            return;
        }

        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            Date datumOd = sdf.parse(txtDatumOd.getText());
            Date datumDo = sdf.parse(txtDatumDo.getText());
            int brojDana = Integer.parseInt(txtBrojDana.getText());
            double iznos = Double.parseDouble(txtIznos.getText());
            Kola kola = (Kola) cmbKola.getSelectedItem();

            StavkaIznajmljivanja si = new StavkaIznajmljivanja(i, -1,
                    datumOd, datumDo, brojDana,
                    kola.getCenaPoDanu(), iznos, kola);

            TableModelStavkeIznajmljivanja tm = (TableModelStavkeIznajmljivanja) tblStavke.getModel();
            tm.dodajStavku(si);

            ukupanIznos = tm.vratiUkupanIznos();
            txtUkupanIznos.setText(String.valueOf(ukupanIznos) + "€");

        } catch (ParseException ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDodajActionPerformed

