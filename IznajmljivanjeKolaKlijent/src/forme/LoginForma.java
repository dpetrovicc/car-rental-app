    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {

            if (txtUsername.getText().isEmpty()
                    || new String(txtPassword.getPassword()).isEmpty()) {
                JOptionPane.showMessageDialog(this, "Korisnicko ime i lozinka moraju "
                        + "biti popunjeni!");
                return;
            }

            Zaposleni z = new Zaposleni();
            z.setKorisnickoIme(txtUsername.getText());
            z.setLozinka(new String(txtPassword.getPassword()));

            Zaposleni zaposleni = ClientController.getInstance().login(z);
            Session.getInstance().setUlogovani(zaposleni);

            new MainForm().setVisible(true);
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }//GEN-LAST:event_btnLoginActionPerformed

